import { execSync } from "child_process";

const INHERIT = {
    stdio: "inherit",
    stderr: "inherit",
};

function get_host() {
    return /host\: (.+?)$/gm.exec(execSync(`rustc -vV`).toString())[1];
}

execSync(`PSP_ROOT_DIR=../.. cargo run --target=${get_host()}`, INHERIT);
