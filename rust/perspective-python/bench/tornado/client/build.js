const esbuild = require("esbuild");
const fs = require("fs");
const path = require("path");
const {
    PerspectiveEsbuildPlugin,
} = require("@finos/perspective-esbuild-plugin");

async function build() {
    await esbuild.build({
        entryPoints: [`${__dirname}/index.js`],
        plugins: [
            PerspectiveEsbuildPlugin({
                worker: {
                    targetdir: path.join(__dirname, "dist", "build"),
                },
            }),
        ],
        outdir: path.join(__dirname, "dist"),
        format: "esm",
        bundle: true,
        sourcemap: true,
        target: "es2022",
        loader: {
            ".ttf": "file",
            ".arrow": "file",
            ".wasm": "file",
        },
        assetNames: "[name]",
    });

    fs.writeFileSync(
        path.join(__dirname, "dist", "index.html"),
        fs.readFileSync(path.join(__dirname, "index.html")).toString()
    );
}

build();
